while True:
    yas = int(input("Yaşınızı giriniz: "))
    if 0<yas<=50:
        print("Yaşınız 0-50 arasındadır.")
        break
    elif 50<yas<=100:
        print("Yaşınız 50-100 arasındadır.")
        break
    else:
        print("Lütfen 0-100 arası bir değer giriniz.")
        
            
